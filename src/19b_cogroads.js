// ---- World-3 travel: Noah WALKS the painted map, and the roads ambush him halfway ----
// (same recipe as the World-1 overworld: point-path, per-leg midpoints, resume on a win)
const W3_CHAIN = ['cog1', 'cog2', 'cog3', 'cog4'];       // the main brass trail
const W3_SPURS = { stable: 'cog1' };                     // side-paths hang off a chain stop (peaceful)
const COG_ROUTES = { 'cog1-cog2': 'gearline', 'cog2-cog3': 'steamway', 'cog3-cog4': 'skyrail' };
const n3 = (id) => WORLD3_NODES.find(n => n.id === id);

Game.world3Go = function (n) {
  if (this.world3Travel) return;                          // already walking
  const hereId = WORLD3_NODES[this.world3Here()].id;
  if (n.exit) { this.state = 'play'; this.loadMap('vale'); saveGame(); return; }
  if (n.id === hereId) { this.state = 'play'; this.loadMap(n.id); saveGame(); return; }
  // node route: spur out -> along the chain -> spur in
  const chainOf = (id) => W3_SPURS[id] || id;
  const route = [hereId];
  if (W3_SPURS[hereId]) route.push(W3_SPURS[hereId]);
  const a = W3_CHAIN.indexOf(chainOf(hereId)), b = W3_CHAIN.indexOf(chainOf(n.id));
  const stp = b > a ? 1 : -1;
  if (a !== b) for (let i = a + stp; stp > 0 ? i <= b : i >= b; i += stp) route.push(W3_CHAIN[i]);
  if (W3_SPURS[n.id]) route.push(n.id);
  const ids = route.filter((id, i) => id !== route[i - 1]);
  // densified straight legs (12 steps each) so every leg has a real midpoint
  const pts = [], legs = [];
  for (let i = 1; i < ids.length; i++) {
    const A = n3(ids[i - 1]), B = n3(ids[i]);
    const startSeg = pts.length ? pts.length - 1 : 0;
    for (let k = pts.length ? 1 : 0; k <= 12; k++) pts.push([lerp(A.x, B.x, k / 12), lerp(A.y, B.y, k / 12)]);
    legs.push({ key: [ids[i - 1], ids[i]].sort().join('-'), startSeg, endSeg: pts.length - 1 });
  }
  this.world3Travel = { pts, seg: 0, x: pts[0][0], y: pts[0][1], target: n.id, anim: 0, dir: 'down', legs };
  Audio2.jingle('step');
};
Game.updateWorld3Travel = function (dt) {
  const tv = this.world3Travel; if (!tv) return;
  tv.anim += dt;
  const next = tv.pts[tv.seg + 1];
  if (!next) {
    const id = tv.target; this.world3Travel = null;
    this.state = 'play'; this.loadMap(id); saveGame(); return;
  }
  const d = dist(tv.x, tv.y, next[0], next[1]);
  const spd = 95 * dt;
  if (d <= spd) {
    tv.x = next[0]; tv.y = next[1]; tv.seg++;
    // TROUBLE ON THE GEAR-ROADS: halfway along each leg, an ambush (once per route)
    for (const leg of tv.legs) {
      const mid = (leg.startSeg + leg.endSeg) >> 1;
      const road = COG_ROUTES[leg.key];
      if (road && tv.seg === mid && !this.flags['road_' + road]) {
        this.pendingCog3 = tv; this.world3Travel = null;
        SideScroll.start(road, true);
        return;
      }
    }
  } else {
    tv.x += (next[0] - tv.x) / d * spd; tv.y += (next[1] - tv.y) / d * spd;
    tv.dir = Math.abs(next[0] - tv.x) > Math.abs(next[1] - tv.y) ? (next[0] > tv.x ? 'right' : 'left') : (next[1] > tv.y ? 'down' : 'up');
  }
};

  // ================= GASKETTA: ceiling-crawler, steam-rain, overheat drops =================
  SS.BOSS_AI.gasketta = function (S, b, dt, ss) {
    const p = S.p;
    const ceilY = (x) => {          // the underside of the arena's pipe-ceiling
      const i = Math.floor(x / ROAD_TILE);
      let j = 0;
      while (j < 6 && !ROAD_SOLID[ss.tile(S, i, j)]) j++;
      if (j >= 6) return 34;
      while (j < 6 && ROAD_SOLID[ss.tile(S, i, j)]) j++;
      return j * ROAD_TILE;
    };
    b.ph = b.ph || 'scuttle'; b.phT = (b.phT || 0) + dt;
    b.rains = b.rains || 0; b.cols = b.cols || []; b.shots = b.shots || [];
    const hangY = ceilY(b.x) + 14;
    if (b.ph === 'scuttle') {
      b.y += clamp(hangY + Math.sin(b.t * 3) * 3 - b.y, -130 * dt, 130 * dt);
      b.x += clamp(p.x - b.x, -64, 64) * dt;
      if (b.phT > 1.2 && !b.thrown && b.rains > 0) {          // one wrench per lap once she's warm
        b.thrown = 1;
        b.shots.push({ x: b.x, y: b.y + 8, vx: clamp((p.x - b.x) * 1.1, -130, 130), vy: 40, t: 0 });
        Audio2.jingle('step');
      }
      if (b.phT > 2.0) {
        if (b.rains >= 2) { b.ph = 'drop'; b.phT = 0; b.vy = 30; Audio2.jingle('denied'); }
        else {
          b.ph = 'rain'; b.phT = 0; b.cols = [];
          const pi = Math.round(p.x / 16);
          for (const off of [0, -3, 3]) b.cols.push({ x: (pi + off) * 16 + 8, t: -0.85 });
          Audio2.jingle('push');
        }
      }
    } else if (b.ph === 'rain') {
      let done = true;
      for (const cl of b.cols) {
        cl.t += dt; if (cl.t < 1.1) done = false;
        if (cl.t > 0 && cl.t < 1.1 && p.inv <= 0 && Math.abs(p.x - cl.x) < 8) ss.hurt(S, 1);
      }
      if (done) { b.ph = 'scuttle'; b.phT = 0; b.thrown = 0; b.rains++; b.cols = []; }
    } else if (b.ph === 'drop') {
      b.vy += 700 * dt; b.y += b.vy * dt;
      const gy = ss.groundYAt(S, b.x);
      if (b.y >= gy) {
        b.y = gy; b.vy = 0; b.ph = 'floor'; b.stun = 2.2; b.rains = 0;
        S.shake = 0.3; Audio2.jingle('rumble'); Particles.burst(b.x, b.y, 'dust');
      }
    } else if (b.ph === 'floor') {                             // stun spent -> fly home
      b.y += clamp(hangY - b.y, -140 * dt, 140 * dt);
      if (Math.abs(b.y - hangY) < 3) { b.ph = 'scuttle'; b.phT = 0; }
    }
    for (let i = b.shots.length - 1; i >= 0; i--) {            // wrenches arc down
      const sh = b.shots[i]; sh.t += dt; sh.vy += 520 * dt; sh.x += sh.vx * dt; sh.y += sh.vy * dt;
      if (sh.y > ss.groundYAt(S, sh.x) + 4 || sh.t > 3) { Particles.burst(sh.x, sh.y, 'dust'); b.shots.splice(i, 1); continue; }
      if (p.inv <= 0 && Math.abs(p.x - sh.x) < 8 && Math.abs((p.y - 8) - sh.y) < 9) { ss.hurt(S, 1); b.shots.splice(i, 1); }
    }
    if (b.ph !== 'floor') ss.bossTouch(S, b, false);           // the ceiling is HERS — no free stomps up there
  };

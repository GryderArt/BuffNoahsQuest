  // ================= PISTON PETE: slam-waves, steam jets, and a fist that gets STUCK =================
  SS.BOSS_AI.pistonpete = function (S, b, dt, ss) {
    const p = S.p, groundY = ss.groundYAt(S, b.x);
    b.vy = (b.vy || 0) + 760 * dt; b.y = Math.min(groundY, b.y + b.vy * dt); if (b.y >= groundY) b.vy = 0;
    b.ph = b.ph || 'stride'; b.phT = (b.phT || 0) + dt;
    b.waves = b.waves || []; b.jets = b.jets || [];
    if (b.ph === 'stride') {
      b.x += Math.sign(p.x - b.x) * 26 * dt;
      if (b.phT > 1.05) { b.ph = 'wind'; b.phT = 0; Audio2.jingle('denied'); }
    } else if (b.ph === 'wind') {
      if (b.phT > 0.5) {
        b.ph = 'stride'; b.phT = 0;
        b.slams = (b.slams || 0) + 1;
        S.shake = 0.3; Audio2.jingle('rumble'); Particles.burst(b.x, b.y, 'dust');
        if (b.slams % 3 === 0) { b.stun = 1.9; b.stuck = 1; }               // the BOP window
        else {
          b.waves.push({ x: b.x, dir: Math.sign(p.x - b.x) || 1, t: 0 });
          if (b.slams % 3 === 2) b.jets.push({ x: Math.round(p.x / 16) * 16 + 8, t: -0.6 });  // steam at YOUR column
        }
      }
    }
    if (b.stun <= 0) b.stuck = 0;
    for (let i = b.waves.length - 1; i >= 0; i--) {     // floor-waves roll along the ground — jump them
      const w = b.waves[i]; w.t += dt; w.x += w.dir * 150 * dt;
      const gy = ss.groundYAt(S, w.x);
      if (w.t > 2.4 || ss.solidAt(S, w.x + w.dir * 6, gy - 8)) { b.waves.splice(i, 1); continue; }
      if (p.inv <= 0 && Math.abs(p.x - w.x) < 8 && p.y > gy - 14) ss.hurt(S, 1);
    }
    for (let i = b.jets.length - 1; i >= 0; i--) {      // warned steam jets, tall enough to cook hoverers
      const jt = b.jets[i]; jt.t += dt;
      if (jt.t > 0.75) { b.jets.splice(i, 1); continue; }
      if (jt.t > 0 && p.inv <= 0) {
        const gy = ss.groundYAt(S, jt.x);
        if (Math.abs(p.x - jt.x) < 9 && p.y > gy - 82) ss.hurt(S, 1);
      }
    }
    // armored head: stomps CLANG off unless the fist is stuck
    if (Math.abs(p.x - b.x) < 17 && Math.abs((p.y - 8) - (b.y - 10)) < 18) {
      if (p.vy > 40 && p.y - 8 < b.y - 8) {
        p.vy = -240; p.inv = Math.max(p.inv, 0.3);
        Audio2.jingle('denied'); Particles.burst(b.x, b.y - 20, 'sparkle');
      } else if (p.inv <= 0) ss.hurt(S, 1);
    }
  };
